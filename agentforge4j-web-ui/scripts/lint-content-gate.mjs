// Committed-content gate (design §4) for this module: scans every `.ts`/`.tsx` source file
// under `src/` plus every other committed, human-authored text file in the module (README,
// the HTML shell, the deployment config, the local Dockerfile) for the product-boundary term
// group. The term list is not authored here — it lives in `agentforge4j-docs/scripts/` and is
// imported via a relative cross-directory path, since no npm workspace ties the OSS repo's
// top-level modules together (confirmed: no root package.json/workspace file). This keeps
// exactly one copy of the list instead of a second, independently-drifting one per module.
//
// `src/` is scanned as `.ts`/`.tsx` only, not `.md`/`.mdx` — this module's page/marketing copy
// lives in TS modules (design §35's "centralised copy in src/copy/"), not markdown, unlike the
// docs site. `EXTRA_FILES` below is an explicit allowlist, not a generic root-directory walk —
// a walk would need to keep excluding `node_modules/`, `dist/`, `package-lock.json`, and the
// `.tsbuildinfo` build caches by pattern forever; a fixed list of the actual committed prose
// surfaces is simpler and fails loudly (an intentionally uncaught `readFileSync` error) if one
// of them is ever renamed or removed without updating this gate, rather than silently narrowing
// coverage.
//
// `GENERATED_FILES` covers `src/generated/catalogue-data.json` — the one non-`.ts`/`.tsx` file
// under `src/` that still carries human-authored prose (each shipped workflow's own
// `author`/`contact`/`description` fields, copied verbatim by build-catalogue-data.mjs from
// agentforge4j-workflows-catalog). It's a build artifact (gitignored, regenerated by
// `npm run catalogue:build`), so this gate must run after that build step, same as every other
// `check` script consumer of this file — `readFileSync` fails loudly if it's missing rather than
// silently skipping it.

import {readdirSync, readFileSync, statSync} from 'node:fs';
import {join, relative, sep, dirname} from 'node:path';
import {fileURLToPath} from 'node:url';
import {findProductNameLeaks, BLOCKED} from '../../agentforge4j-docs/scripts/product-name.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const MODULE_ROOT = join(here, '..');
const SRC_DIR = join(MODULE_ROOT, 'src');
const SOURCE_EXTENSIONS = ['.ts', '.tsx'];
const EXTRA_FILES = [
  'README.md',
  'index.html',
  'nginx.conf',
  'Dockerfile.local',
  'public/robots.txt',
  'src/config/seo-routes.json',
];
const GENERATED_FILES = ['generated/catalogue-data.json'];

function collectSources(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      out.push(...collectSources(full));
    } else if (SOURCE_EXTENSIONS.some((ext) => entry.endsWith(ext))) {
      out.push(full);
    }
  }
  return out;
}

const targets = [
  ...collectSources(SRC_DIR),
  ...EXTRA_FILES.map((f) => join(MODULE_ROOT, f)),
  ...GENERATED_FILES.map((f) => join(SRC_DIR, f)),
];

let violations = 0;
let files = 0;
for (const file of targets) {
  files += 1;
  const rel = relative(MODULE_ROOT, file).split(sep).join('/');
  const text = readFileSync(file, 'utf8');

  for (const {token, line, excerpt} of findProductNameLeaks(text)) {
    console.error(`✗ ${rel}:${line} — commercial identifier '${token}': ${excerpt}`);
    violations += 1;
  }
}

if (violations > 0) {
  console.error(
    `\ncontent-gate: ${violations} violation(s) across ${files} file(s). See ` +
      'agentforge4j-docs/scripts/product-name.mjs for the reviewed term list.',
  );
  process.exit(1);
}

console.log(`content-gate: ${files} file(s) clean of ${BLOCKED.length} product-boundary pattern(s).`);

// --- isWithinRoot: the containment rule a symlinked directory/file's resolved target must satisfy
// before this module will follow it — directly testable without creating a real symlink (which
// requires elevated privileges on Windows, an environment concern this logic must not depend on to
// be verifiable) ---

test('isWithinRoot: a real descendant path is within root', () => {
  const root = join(tmpdir(), 'javadoc-root');
  assert.ok(isWithinRoot(root, join(root, 'com', 'example', 'Foo.html')));
  assert.ok(isWithinRoot(root, root));
});

test('isWithinRoot: a path reached via `..` traversal, or an unrelated sibling directory sharing the same string prefix, is rejected', () => {
  const root = join(tmpdir(), 'javadoc-root');
  assert.ok(!isWithinRoot(root, join(tmpdir(), 'secret.txt')));
  assert.ok(!isWithinRoot(root, join(tmpdir(), 'javadoc-root-evil', 'secret.txt')));
});

test('a symlinked directory pointing outside the surface root is refused, not silently followed (skipped in environments where creating a symlink requires elevated privileges, e.g. unconfigured Windows)', () => {
  const root = mkdtempSync(join(tmpdir(), 'javadoc-symlink-'));
  const siteDir = join(root, '_site');
  const surfaceRoot = join(siteDir, 'javadoc', 'next');
  mkdirSync(surfaceRoot, { recursive: true });
  writeFileSync(join(surfaceRoot, 'index.html'), RAW_OVERVIEW_HTML, 'utf8');
  const outsideDir = join(root, 'outside-the-surface');
  mkdirSync(outsideDir, { recursive: true });
  writeFileSync(join(outsideDir, 'secret.html'), RAW_OVERVIEW_HTML, 'utf8');

  let symlinkCreated = true;
  try {
    symlinkSync(outsideDir, join(surfaceRoot, 'escape'), 'dir');
  } catch (error) {
    if (error.code === 'EPERM' || error.code === 'EACCES') {
      symlinkCreated = false;
    } else {
      throw error;
    }
  }
  if (!symlinkCreated) {
    console.log('[javadoc-seo.test] symlink creation requires elevated privileges in this environment — skipping the live symlink assertion (isWithinRoot itself is covered by the two tests above)');
    return;
  }

  assert.throws(
    () =>
      applyJavadocSeo({
        siteDir,
        siteUrl: 'https://agentforge4j.org',
        ogImage: 'https://agentforge4j.org/brand/icon-512.png',
        releasedVersions: [],
      }),
    /refusing to follow a symlink that escapes the surface root/,
  );
});

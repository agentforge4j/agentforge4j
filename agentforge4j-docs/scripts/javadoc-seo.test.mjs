// A trimmed but structurally real sample — confirmed against a live-built /javadoc/0.1.0/ overview
// page: the exact `<html lang>` (no value) and `content="module index"` shapes maven-javadoc-plugin
// emits, with everything else maven-javadoc-plugin actually ships around them.
const RAW_OVERVIEW_HTML = `<!DOCTYPE HTML>
<html lang>
<head>
<!-- Generated by javadoc (17) on Wed Jul 22 05:02:14 UTC 2026 -->
<title>Overview (AgentForge4j API (next))</title>
<meta name="viewport" content="width=device-width, initial-scale=1">
<meta http-equiv="Content-Type" content="text/html; charset=UTF-8">
<meta name="dc.created" content="2026-07-22">
<meta name="description" content="module index">
<meta name="generator" content="javadoc/ModuleIndexWriter">
<link rel="stylesheet" type="text/css" href="stylesheet.css" title="Style">
</head>
<body class="module-index-page">
<div class="header"><h1 class="title">AgentForge4j API (next)</h1></div>
</body>
</html>
`;

// A trimmed but structurally real sample of a class page — confirmed against a real `javadoc`
// (JDK 17) run: unlike the overview page above, nested pages already carry a correct
// `<html lang="en">` and a real (if generic-shaped) description — the known empty-lang/"module
// index" bug is specific to the overview page, never assumed present elsewhere.
function rawClassPageHtml({ title = 'Foo', description = 'declaration: package: com.example, class: Foo' } = {}) {
  return `<!DOCTYPE HTML>
<html lang="en">
<head>
<!-- Generated by javadoc (17) on Wed Jul 22 23:24:12 CEST 2026 -->
<title>${title}</title>
<meta name="viewport" content="width=device-width, initial-scale=1">
<meta http-equiv="Content-Type" content="text/html; charset=utf-8">
<meta name="dc.created" content="2026-07-22">
<meta name="description" content="${description}">
<meta name="generator" content="javadoc/ClassWriterImpl">
<link rel="stylesheet" type="text/css" href="../../stylesheet.css" title="Style">
</head>
<body class="class-declaration-page">
<main role="main"><h1 class="title">Class Foo</h1><div class="member-signature">public class <span class="element-name">Foo</span></div></main>
</body>
</html>
`;
}


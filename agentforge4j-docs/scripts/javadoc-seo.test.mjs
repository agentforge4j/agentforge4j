test('injectJavadocPageSeo fixes lang, replaces the generic description, and adds canonical/OG/Twitter (none of which exist in the raw overview output)', () => {
  const html = injectJavadocPageSeo(RAW_OVERVIEW_HTML, {
    title: 'AgentForge4j API Reference — latest stable, 0.1.0',
    description: 'Generated Javadoc API reference for the AgentForge4j framework (latest stable, 0.1.0).',
    canonical: 'https://agentforge4j.org/javadoc/latest/',
    ogImage: 'https://agentforge4j.org/brand/icon-512.png',
    noindex: false,
  });
  assert.match(html, /<html lang="en">/);
  assert.doesNotMatch(html, /<html lang>/);
  assert.doesNotMatch(html, /content="module index"/);
  assert.match(html, /<meta name="description" content="Generated Javadoc API reference[^"]*">/);
  assert.match(html, /<link rel="canonical" href="https:\/\/agentforge4j\.org\/javadoc\/latest\/">/);
  assert.match(html, /<meta property="og:url" content="https:\/\/agentforge4j\.org\/javadoc\/latest\/">/);
  assert.match(html, /<meta property="og:image" content="https:\/\/agentforge4j\.org\/brand\/icon-512\.png">/);
  assert.match(html, /<meta name="twitter:card" content="summary">/);
  assert.doesNotMatch(html, /<meta name="robots"/);
  // Real Javadoc functionality (navigation, generated body) must survive untouched.
  assert.match(html, /<h1 class="title">AgentForge4j API \(next\)<\/h1>/);
});

test('injectJavadocPageSeo leaves an already-correct lang="en" untouched on a nested page (only the overview page has the empty-lang bug — never assumed present elsewhere)', () => {
  const html = injectJavadocPageSeo(rawClassPageHtml(), {
    title: 'Foo — AgentForge4j API Reference (next)',
    description: 'Generated Javadoc API reference for the AgentForge4j framework (next) — Foo.',
    canonical: 'https://agentforge4j.org/javadoc/next/com/example/Foo.html',
    ogImage: 'https://agentforge4j.org/brand/icon-512.png',
  });
  assert.match(html, /<html lang="en">/);
  // Real generated navigation/body content must survive untouched.
  assert.match(html, /<h1 class="title">Class Foo<\/h1>/);
  assert.match(html, /public class <span class="element-name">Foo<\/span>/);
});


test('injectJavadocPageSeo fails closed with a clear "already processed" error rather than silently duplicating tags on a page that already has a canonical', () => {
  const once = injectJavadocPageSeo(RAW_OVERVIEW_HTML, {
    title: 't',
    description: 'd',
    canonical: 'https://agentforge4j.org/javadoc/next/',
    ogImage: 'https://agentforge4j.org/brand/icon-512.png',
  });
  assert.throws(
    () =>
      injectJavadocPageSeo(once, {
        title: 't2',
        description: 'd2',
        canonical: 'https://agentforge4j.org/javadoc/next/',
        ogImage: 'https://agentforge4j.org/brand/icon-512.png',
      }),
    /already has a <link rel="canonical"> tag — refusing to insert duplicate SEO tags/,
  );
  // And no duplicate tags were actually written to `once` by the first (successful) call itself.
  assert.equal((once.match(/<link rel="canonical"/g) ?? []).length, 1);
  assert.equal((once.match(/<meta property="og:title"/g) ?? []).length, 1);
});

test('injectJavadocPageSeo escapes adversarial title/description/canonical/image values in every attribute it writes', () => {
  const html = injectJavadocPageSeo(RAW_OVERVIEW_HTML, {
    title: 'Evil"><script>alert(1)</script>',
    description: 'desc & "quoted" <tag>',
    canonical: 'https://agentforge4j.org/javadoc/next/?x="><script>alert(2)</script>',
    ogImage: 'https://agentforge4j.org/brand/icon-512.png?x="onerror=alert(3)',
  });
  assert.ok(!/<script>/.test(html), 'no live <script> element may appear anywhere in the generated output');
  // The dangerous quote in the middle of the image value must itself be escaped (not the tag's own
  // legitimate closing quote, which always renders as `"` regardless of escaping).
  assert.match(html, /<meta property="og:image" content="https:\/\/agentforge4j\.org\/brand\/icon-512\.png\?x=&quot;onerror=alert\(3\)">/);
  assert.match(html, /<meta property="og:title" content="Evil&quot;&gt;&lt;script&gt;alert\(1\)&lt;\/script&gt;">/);
  assert.match(html, /<meta name="description" content="desc &amp; &quot;quoted&quot; &lt;tag&gt;">/);
});

function fixtureSiteDir(surfaces) {
  const root = mkdtempSync(join(tmpdir(), 'javadoc-seo-'));
  const siteDir = join(root, '_site');
  for (const mountPath of surfaces) {
    const dir = join(siteDir, ...mountPath.split('/'));
    mkdirSync(dir, { recursive: true });
    writeFileSync(join(dir, 'index.html'), RAW_OVERVIEW_HTML, 'utf8');
  }
  return siteDir;
}

/** Every real applyJavadocSeo call always processes `javadoc/next`, `javadoc/latest`, and one
 * `javadoc/<v>` per `releasedVersions` entry — this fixture always creates that full, real set (a
 * plain overview-only page for the surfaces not under test), with `targetMountPath` given a real
 * nested tree (package summary, class page, all-classes index) two levels deep — the same shape a
 * real `javadoc` (JDK 17) run produces for a single package — so tests can assert on nested-page
 * behavior for one specific surface without applyJavadocSeo failing on the other, unrelated,
 * always-required surfaces. */
function fixtureSiteDirWithNestedPages(targetMountPath, releasedVersions = ['0.1.0']) {
  const root = mkdtempSync(join(tmpdir(), 'javadoc-seo-nested-'));
  const siteDir = join(root, '_site');
  const allMountPaths = ['javadoc/next', 'javadoc/latest', ...releasedVersions.map((version) => `javadoc/${version}`)];
  for (const mountPath of allMountPaths) {
    const dir = join(siteDir, ...mountPath.split('/'));
    mkdirSync(dir, { recursive: true });
    writeFileSync(join(dir, 'index.html'), RAW_OVERVIEW_HTML, 'utf8');
  }
  const surfaceRoot = join(siteDir, ...targetMountPath.split('/'));
  mkdirSync(join(surfaceRoot, 'com', 'example'), { recursive: true });
  writeFileSync(join(surfaceRoot, 'index.html'), RAW_OVERVIEW_HTML, 'utf8');
  writeFileSync(join(surfaceRoot, 'allclasses-index.html'), rawAllClassesIndexHtml(), 'utf8');
  writeFileSync(join(surfaceRoot, 'com', 'example', 'package-summary.html'), rawPackageSummaryHtml(), 'utf8');
  writeFileSync(join(surfaceRoot, 'com', 'example', 'Foo.html'), rawClassPageHtml(), 'utf8');
  // A non-HTML asset alongside — must remain byte-identical.
  writeFileSync(join(surfaceRoot, 'stylesheet.css'), 'body { color: black; }\n', 'utf8');
  return { siteDir, surfaceRoot };
}

test('applyJavadocSeo updates next, latest, and every released version\'s overview page — self-canonical, indexable', () => {
  const siteDir = fixtureSiteDir(['javadoc/next', 'javadoc/latest', 'javadoc/0.1.0']);
  const updated = applyJavadocSeo({
    siteDir,
    siteUrl: 'https://agentforge4j.org',
    ogImage: 'https://agentforge4j.org/brand/icon-512.png',
    releasedVersions: ['0.1.0'],
  });
  assert.equal(updated, 3);

  const next = readFileSync(join(siteDir, 'javadoc', 'next', 'index.html'), 'utf8');
  assert.match(next, /<link rel="canonical" href="https:\/\/agentforge4j\.org\/javadoc\/next\/">/);
  assert.doesNotMatch(next, /<meta name="robots"/);

  const latest = readFileSync(join(siteDir, 'javadoc', 'latest', 'index.html'), 'utf8');
  assert.match(latest, /<link rel="canonical" href="https:\/\/agentforge4j\.org\/javadoc\/latest\/">/);
  assert.doesNotMatch(latest, /<meta name="robots"/);

  // 0.1.0 IS the version /latest/ currently mirrors (releasedVersions[0]) — noindex,follow to avoid
  // two indexable URLs with byte-identical content, per the task's own chosen policy.
  const pinned = readFileSync(join(siteDir, 'javadoc', '0.1.0', 'index.html'), 'utf8');
  assert.match(pinned, /<link rel="canonical" href="https:\/\/agentforge4j\.org\/javadoc\/0\.1\.0\/">/);
  assert.match(pinned, /<meta name="robots" content="noindex,follow">/);
});

test('applyJavadocSeo processes every nested page in the tree (package summary, class page, all-classes index), not only the surface overview', () => {
  const { siteDir } = fixtureSiteDirWithNestedPages('javadoc/next');
  const updated = applyJavadocSeo({
    siteDir,
    siteUrl: 'https://agentforge4j.org',
    ogImage: 'https://agentforge4j.org/brand/icon-512.png',
    releasedVersions: [],
  });
  // 4 pages under javadoc/next (index, allclasses-index, package-summary, Foo) + 1 under
  // javadoc/latest (its own plain overview, always processed alongside next) = 5.
  assert.equal(updated, 5, 'expected exactly the 5 real .html pages across next+latest in this fixture to be processed');

  const packageSummary = readFileSync(join(siteDir, 'javadoc', 'next', 'com', 'example', 'package-summary.html'), 'utf8');
  assert.match(packageSummary, /<link rel="canonical" href="https:\/\/agentforge4j\.org\/javadoc\/next\/com\/example\/package-summary\.html">/);
  assert.match(packageSummary, /<meta property="og:title"/);

  const classPage = readFileSync(join(siteDir, 'javadoc', 'next', 'com', 'example', 'Foo.html'), 'utf8');
  assert.match(classPage, /<link rel="canonical" href="https:\/\/agentforge4j\.org\/javadoc\/next\/com\/example\/Foo\.html">/);

  const allClasses = readFileSync(join(siteDir, 'javadoc', 'next', 'allclasses-index.html'), 'utf8');
  assert.match(allClasses, /<link rel="canonical" href="https:\/\/agentforge4j\.org\/javadoc\/next\/allclasses-index\.html">/);
});

